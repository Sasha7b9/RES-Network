#define SENS_ID			6

/* Private variables ---------------------------------------------------------*/
I2C_HandleTypeDef hi2c1;
UART_HandleTypeDef huart1;

char buffer_USB[60] = {0};
uint8_t radio_buf[6] = {0};
float valueLux = 0;

/* Private function prototypes -----------------------------------------------*/
void SystemClock_Config(void);
static void MX_GPIO_Init(void);
