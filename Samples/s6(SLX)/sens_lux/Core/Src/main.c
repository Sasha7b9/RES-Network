void pack2BytesSFloat(uint8_t index, double bytes)
{
	float  tmp_fraction;
	double tmp_integer;

	tmp_fraction = modf(bytes, &tmp_integer);
	radio_buf[index] = (int8_t)tmp_integer;
	radio_buf[index+1] = (int8_t)(100 * tmp_fraction);
}
uint16_t tmpVal;
void pack2BytesInt(uint8_t index, double bytes)
{
	 tmpVal = bytes;
	memcpy(&radio_buf[index], &tmpVal, 2);
}

uint8_t calcTelemetryCrc(void)
{
    uint8_t crc = 0xFF;
    for(uint8_t i = 0; i < 5; i++){
        crc ^= radio_buf[i];
        for(uint8_t j = 0; j < 4; j++)
            crc = crc & 0x80 ? (crc << 1) ^ 0x31 : crc << 1;
    }
    return crc;
}



int main(void)
{
  HAL_Init();
  SystemClock_Config();

  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_USART1_UART_Init();
  MX_USB_DEVICE_Init();

  while (1)
  {
		HAL_Delay(400);
		BH1750_Start();
		HAL_Delay(600);
		valueLux = BH1750_GetLumen();

		radio_buf[0] = SENS_ID;
		pack2BytesInt(1, valueLux);
		pack2BytesInt(3, valueLux);
		radio_buf[5] = calcTelemetryCrc();

		memcpy(&tmpVal, &radio_buf[1], 2);

		memset(buffer_USB,0,60);
		sprintf(buffer_USB, "SLX:%i\n", (int)valueLux);
		CDC_Transmit_FS((uint8_t*)buffer_USB, strlen(buffer_USB));
		HAL_UART_Transmit(&huart1, radio_buf, 6, 1000);
  }
}

