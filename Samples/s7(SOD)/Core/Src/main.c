/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  HAL_Init();
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_USART1_UART_Init();
  MX_USB_DEVICE_Init();



  while (1)
  {
		HAL_ADC_Start(&hadc1);
		HAL_ADC_PollForConversion(&hadc1, 100);
		valMeas = ADC_Convert( HAL_ADC_GetValue(&hadc1));
		HAL_ADC_Stop(&hadc1);

		radio_buf[0] = SENS_ID;
		pack2BytesInt(1, valMeas);
		pack2BytesInt(3, valMeas);
		radio_buf[5] = calcTelemetryCrc();

		memset(buffer_USB,0,60);
		sprintf(buffer_USB, "SOD:%i\n", (uint16_t)valMeas);
		CDC_Transmit_FS((uint8_t*)buffer_USB, strlen(buffer_USB));
		HAL_UART_Transmit(&huart1, radio_buf, 6, 1000);

		HAL_Delay(1000);
  }
  /* USER CODE END 3 */
}

