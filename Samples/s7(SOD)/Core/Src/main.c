void pack2BytesSFloat(uint8_t index, double bytes)
{
	float  tmp_fraction;
	double tmp_integer;

	tmp_fraction = modf(bytes, &tmp_integer);

	radio_buf[index] = (int8_t)tmp_integer;
	radio_buf[index+1] = (int8_t)(100 * tmp_fraction);
}
uint16_t tmpVal;
void pack2BytesInt(uint8_t index, double bytes)
{
	tmpVal = bytes;
	memcpy(&radio_buf[index], &tmpVal, 2);
}

uint8_t calcTelemetryCrc(void)
{
    uint8_t crc = 0xFF;

    for(uint8_t i = 0; i < 5; i++)
    {
        crc ^= radio_buf[i];

        for(uint8_t j = 0; j < 4; j++)
            crc = crc & 0x80 ? (crc << 1) ^ 0x31 : crc << 1;
    }
    return crc;
}

float ADC_Convert(uint16_t value)
{
	#define OXID_MAX	3600	//25%
	#define OXID_MIN	1020	//20%
	#define OXID_ZERO	0		//0%
	float oxid = 0;

	if(value > OXID_MAX) value = OXID_MAX;
	if(value >= OXID_MIN) oxid = 20.0 + 5.0*(value - OXID_MIN) / (OXID_MAX - OXID_MIN);			//20% + 5%*...
	else oxid = value*20.0/OXID_MIN;		//20.0%

	return oxid;
}
